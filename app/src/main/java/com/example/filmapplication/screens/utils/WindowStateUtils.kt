package com.example.filmapplication.screens.utils

/**
 * Enumeration representing different navigation styles in the Oogarts application.
 */
enum class FilmApplicationNavigationType {
    /**
     * Bottom navigation bar for switching between app sections.
     */
    BOTTOM_NAVIGATION,

    /**
     * Vertical navigation rail for larger screens or tablets.
     */
    NAVIGATION_RAIL,

    /**
     * Permanent navigation drawer always visible on the screen.
     */
    PERMANENT_NAVIGATION_DRAWER
}
